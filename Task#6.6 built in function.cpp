#include<iostream>
#include<math.h>
using namespace std;

int main()
{
	int n;
	float f;
	cout<<"Enter an integer: "<<endl;
	cin>>n;
	cout<<"Enter a floating point number: "<<endl;
	cin>>f;
	
	cout<<"The absolute value of integer "<<n<<" is: "<<abs(n)<<endl;
	cout<<"The absolute value of floating point number "<<f<<" is: "<<fabs(f)<<endl;
	
}

//math function
//abs() absolute value of integer
//fabs() absolute value of floating point number
