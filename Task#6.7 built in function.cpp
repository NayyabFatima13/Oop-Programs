#include<iostream>
#include<math.h>
using namespace std;

int main()
{
	double d;
	cout<<"Enter a double value: "<<endl;
	cin>>d;
	
	cout<<"The rounded value with ceil() = "<<ceil(d)<<endl;
	cout<<"The rounded value with floor() = "<<floor(d)<<endl;	
	return 0;	
}
